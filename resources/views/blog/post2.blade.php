                                <div class="card-body p-0 text-justify">
                                    <p>The government's recent introduction of Minister of Finance Regulation Number 79 of 2023 (PMK-79/2023) focuses on providing technical guidelines for fair and legally certain valuation procedures for taxation purposes. The regulation targets two main objectives: determining the value of land and building tax (PBB) objects and establishing the value of tangible assets, intangible assets, and businesses.</p>
                                    <p>PBB valuation can be done for the current or preceding fiscal year through either office or field valuation. Office valuation involves analyzing taxpayer-submitted data, leading to the issuance of Land and Building Tax Due Notifications. Field valuation entails activities such as supervision, examination, objection settlement, and investigation, with results serving as the basis for various PBB calculations.</p>
                                    <p>Similarly, the valuation of tangible assets, intangible assets, and businesses can be conducted through office or field methods. Both types of valuation play a role in tax audit, Mutual Agreement Procedures, Advance Price Agreements, objection settlement, tax determination reduction or cancellation, collection, preliminary evidence examination, and investigation within the taxation field.</p>
                                    <p>Tangible assets subject to valuation include land, buildings, machinery, vehicles, and various equipment. Intangible assets eligible for valuation encompass marketing, customer relations, technology, and goodwill. The valuation for business entities covers participation in open or closed companies, financial instruments, and the fairness of financial statements.</p>  
                                    <p>In conclusion, PMK-79/2023 is a significant step towards ensuring fairness and legal certainty in taxation valuation. The regulation outlines detailed procedures for assessing PBB objects, tangible assets, intangible assets, and businesses. Through both office and field valuations, the government aims to establish a transparent and just tax system, enhancing the overall effectiveness and integrity of the taxation framework. This initiative highlights a commitment to reinforce the foundation for a robust and equitable fiscal landscape.</p>

